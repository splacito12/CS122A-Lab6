int rotateMario = 0;

//for exercise 3, we will be using a timer_interrupt for the rotation of 90 degrees every second
bool timer_interrupt(struct repeating_timer *t){
    //Like mario said, we want data to be recieved on every positive edge/rising edge
    //therefore, when sending our 16bits one by one, we need to send them while our CS is low
    gpio_put(CS, 0);
    
    //we need to consider top to bottom and side to side for the animation aka, row and column
    for(int i = 0; i < 16; i++){
        for(int j = 0; j < 16; j++){
            int k = 0;

            if(rotateMario == 0){
                k = i*16 + j; //original
            }
            else if(rotateMario == 1){
                k = (15-j)*16 + i; //90 degrees clockwise
            }
            else if(rotateMario == 2){
                k = (15-i)*16 + (15-j); //180 degrees
            }
            else if(rotateMario == 3){
                k = j*16 + (15-i); //90 degrees counterclockwise
            }

            //get the high and low byte
            uint8_t high_byte = (mario[k] >> 8) & 0xFF; 
            uint8_t low_byte = mario[k] & 0xFF;

            //send the high and low byte separately
            spi_write_blocking(spi0, &high_byte, 1); 
            spi_write_blocking(spi0, &low_byte, 1);
        }
    }
    gpio_put(CS, 1); // Set CS high
    rotateMario = (rotateMario + 1) % 4; // Update the rotation state

    return true;
}

